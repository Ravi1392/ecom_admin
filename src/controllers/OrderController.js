const Order = require("../models/orderModel");
const Product = require("../models/productModel");
const ErrorHander = require("../../utils/errorhander");
const catchAsynErrors = require("../middleware/catchAsyncErrors");

//Create New order
exports.createOrder = catchAsynErrors(async (req, res, next) => {
  const {
    shippingInfo,
    orderItems,
    paymentInfo,
    itemsPrice,
    taxPrice,
    shippingPrice,
    totalPrice,
  } = req.body;

  const CreateOrder = await Order.create({
    shippingInfo,
    orderItems,
    paymentInfo,
    itemsPrice,
    taxPrice,
    shippingPrice,
    totalPrice,
    paidAt: Date.now(),
    user: req.user._id,
  });

  res.status(201).json({
    success: true,
    CreateOrder,
  });
});

